        <div class="info-ecoscore">
            <h2>Wat is de Ecoscore?</h2>
            <p>De ecoscore is een meetinstrument dat voedingsproducten beoordeelt op hun duurzaamheid. Het systeem is onderverdeeld in vijf categorieën, variërend van <strong>A</strong> (donkergroen, zeer duurzaam) tot <strong>E</strong> (rood, minst duurzaam). De score wordt berekend op basis van verschillende factoren, zoals productie, transport, verpakking, en milieueffecten zoals CO₂-uitstoot en watergebruik.</p>

            <h3>Hoe wordt de ecoscore berekend?</h3>
            <ul>
                <li><strong>Levenscyclusanalyse (LCA):</strong> Een evaluatie van de totale milieu-impact gedurende de hele levenscyclus van een product, van productie tot afvalverwerking.</li>
                <li><strong>Bonus-malussysteem:</strong> Extra punten voor milieuvriendelijke praktijken zoals biologische productie, en puntenaftrek voor schadelijke effecten zoals ontbossing of overmatige verpakking.</li>
            </ul>
        </div>

        <div class="impact-milieu">
            <h2>De Impact van Voedsel op het Milieu</h2>
            <p>De voedselproductie is wereldwijd verantwoordelijk voor ongeveer een kwart van de totale broeikasgasuitstoot. Factoren zoals landgebruik, watergebruik, en transport dragen significant bij aan de ecologische voetafdruk van voedsel. Dierlijke producten zoals vlees en zuivel hebben doorgaans een hogere impact dan plantaardige alternatieven.</p>

            <h3>Belangrijke factoren:</h3>
            <ul>
                <li><strong>Landgebruik:</strong> Veeteelt en grootschalige landbouw leiden vaak tot ontbossing en verlies van biodiversiteit.</li>
                <li><strong>Watergebruik:</strong> Voor de productie van 1 kilogram rundvlees is gemiddeld 15.000 liter water nodig, terwijl voor 1 kilogram tarwe slechts 1.500 liter nodig is.</li>
                <li><strong>Transport:</strong> Hoewel transport een kleinere impact heeft dan productie, kan het voor geïmporteerde producten aanzienlijk bijdragen aan de voetafdruk.</li>
                <li><strong>Voedselverspilling:</strong> Jaarlijks wordt wereldwijd ongeveer een derde van al het voedsel verspild, wat onnodig bijdraagt aan de milieu-impact.</li>
            </ul>
